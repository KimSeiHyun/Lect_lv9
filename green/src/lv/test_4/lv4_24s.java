package lv.test_4;

import java.util.Scanner;

public class lv4_24s {

	public static void main(String[] args) {
		//문제1) 1~백만 사이의 숫자를 입력받고 가운데 숫자 출력 
        //(단 ! 짝수자리의 수는 짝수자리라고 출력)
		// 예)  123 ==> 2
		// 예)  1234 ==> 짝수의 자리이다
		// 예)  1 ==> 1
		// 예)  1234567 ==> 4
		
		// 힌트 자리수를 구하고 배열을 만든다음 하나씩 저장 
		// 예) ==> 123 ==> 3 ==> arr[] = new int[3];
		
		Scanner sc = new Scanner(System.in);
		
		String x = sc.next();
		int arr[] = new int[x.length()];
		
		for(int i=0; i<arr.length; i++) {
			String a = x.charAt(i)+"";
			arr[i] = Integer.parseInt(a);
			System.out.print(arr[i] + " ");
		}System.out.println();
		
		if(arr.length%2 == 0) {
			System.out.println("짝수의 자리이다.");
		}else {
			System.out.println(arr[x.length()/2]);
		}
	
	}

}
